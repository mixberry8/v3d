V3D_RESULT ToV3DResult(VkResult value)
{
	switch (value)
	{
	case VK_SUCCESS:
	case VK_EVENT_SET:
	case VK_EVENT_RESET:
		return V3D_OK;

	case VK_NOT_READY:
		return V3D_NOT_READY;

	case VK_TIMEOUT:
		return V3D_TIMEOUT;

	case VK_ERROR_OUT_OF_HOST_MEMORY:
		return V3D_ERROR_OUT_OF_HOST_MEMORY;

	case VK_ERROR_OUT_OF_DEVICE_MEMORY:
		return V3D_ERROR_OUT_OF_DEVICE_MEMORY;

	case VK_ERROR_DEVICE_LOST:
		return V3D_ERROR_DEVICE_LOST;

	case VK_ERROR_FORMAT_NOT_SUPPORTED:
		return V3D_ERROR_NOT_SUPPORTED;

	default:
		return V3D_ERROR_FAIL;
	}
}

